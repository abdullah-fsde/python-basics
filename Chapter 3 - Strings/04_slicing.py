a = "Abdullah"
print(a[0:3])
print(a[-1:-4]) # This will return an empty string since the start index is greater than the end index
print(a[-4:-1])
print(a[0:5:2]) # This will print every second character from index 0 to 4
print(a[::2]) # This will print every second character from the entire string
print(a[::-1]) # This will reverse the string
print(a[::1])
print(a[:-1])
print(a[1:-1])
